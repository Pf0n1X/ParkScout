        // Inflate the layout for this fragment
        val rootView = inflater.inflate(R.layout.fragment_add_park, container, false)
        var selectPhotoBtn =
            rootView.findViewById(R.id.uploadPhotobBtn) as Button

        selectPhotoBtn.setOnClickListener {
            selectPhoto()
        }
